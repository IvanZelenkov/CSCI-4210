class Product:
    def __init__(self, name, image_url, color, store_name, price, brand, store_link, store_location, rating):
        self.name = name
        self.image_url = image_url
        self.color = color
        self.store_name = store_name
        self.price = price
        self.brand = brand
        self.store_link = store_link
        self.store_location = store_location
        self.rating = rating
